# Helper function that converts the "real world coordinate" to node coordinate for A* grid ----
def coordinates2nodes(xy):
    rounded_xy = round(xy*4)/4
    node_xy = int(rounded_xy/0.25)
    return node_xy

# Helper function that converts the node coordinate to "real world coordinate" for A* grid ----
def nodes2coordinates(xy):
    coordinate_xy = xy/4
    return coordinate_xy

